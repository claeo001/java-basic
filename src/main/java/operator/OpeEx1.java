package operator;

/*
    산술 : + - * / % << >>
    비교 : > < >= <= == !=
    논리 : && || ! & | ^ ~
    기타 : (type) ?: instanceof
 */
public class OpeEx1 {
    public static void main(String[] args) {
        int i = 5;
        i++;
        System.out.println(i);

        i = 5;
        ++i;
        System.out.println(i);
    }
}
